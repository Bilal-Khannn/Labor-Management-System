package com.example.sdaproject;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Scene;
import javafx.scene.image.Image;
import javafx.stage.Stage;

import java.io.IOException;

public class MainApplication extends Application {
    @Override
    public void start(Stage stage) throws IOException {
        FXMLLoader fxmlLoader = new FXMLLoader(MainApplication.class.getResource("login.fxml"));
        Scene scene = new Scene(fxmlLoader.load(), 1366, 700);
        stage.setTitle("L M S");
        Image image = new Image("C:\\Users\\786-786\\Desktop\\IntelliJ Projects\\SDA-FinalProject\\src\\main\\resources\\com\\example\\sdaproject\\applogo.jpeg");
        stage.getIcons().add(image);
        stage.setScene(scene);
        stage.show();
    }

    public static void main(String[] args) {
        launch();
    }
}